#include "libft.h"

void	ft_splitdel(char ***split)
{
	int		i;

	i = 0;
	while ((*split)[i])
	{
		free((*split)[i]);
		(*split)[i] = NULL;
		i++;
	}
	ft_memdel((void **)split);
}
